function openDirections() {
    // Store address - you can update this with your actual address
    const address = "Electric Shop, Main Street, Your City, State, PIN";
    const encodedAddress = encodeURIComponent(address);
    
    // Check if it's a mobile device
    const isMobile = /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent);
    
    if (isMobile) {
        // For mobile devices, try to open the default map app
        // This will work with Google Maps, Apple Maps, or other map apps installed
        const mapUrl = `https://maps.google.com/maps?q=${encodedAddress}`;
        window.open(mapUrl, '_blank');
    } else {
        // For desktop, open Google Maps in a new tab
        const mapUrl = `https://www.google.com/maps/search/?api=1&query=${encodedAddress}`;
        window.open(mapUrl, '_blank');
    }
}

function closePopup() {
    const overlay = document.querySelector('.popup-overlay');
    if (overlay) {
        overlay.style.animation = 'fadeOut 0.3s ease';
        setTimeout(() => {
            if (overlay.parentNode) {
                overlay.parentNode.removeChild(overlay);
            }
        }, 300);
    }
}

function addScrollEffects() {
    // Add scroll animations to elements
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, observerOptions);

    // Observe all products, feature cards, contact cards, repair cards, wire cards, and company cards
    document.querySelectorAll('.product, .feature-card, .contact-info-card, .repair-card, .wire-card, .company-card').forEach(el => {
        el.style.opacity = '0';
        el.style.transform = 'translateY(30px)';
        el.style.transition = 'all 0.6s ease';
        observer.observe(el);
    });
}

